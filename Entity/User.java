package Entity;

public class User {

	String name;
	String password;
	
	public void setName(String Name){
		this.name = Name;
	}
	
	public void setPassword(String Password){
		this.password = Password;
	}
	
	public String getName(){
		return name;
	}
	
	public String getPassword(){
		return password;
	}
}
